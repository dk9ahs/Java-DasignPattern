
public class Age {

	int year;
	int vlaue;
	
	public Age(int year, int vlaue) {
		super();
		this.year = year;
		this.vlaue = vlaue;
	}
	
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getVlaue() {
		return vlaue;
	}
	public void setVlaue(int vlaue) {
		this.vlaue = vlaue;
	}
	
	
}
